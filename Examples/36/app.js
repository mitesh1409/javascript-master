/**
 * Find the index of a specific element in an array using indexOf() array method.
 */
const numbers = [10, 20, 30, 40, 50];

console.log('numbers array', numbers);
console.log('Index of element 30:', numbers.indexOf(30));
console.log('Index of element 50:', numbers.indexOf(50));
console.log('Index of element 60:', numbers.indexOf(60));
